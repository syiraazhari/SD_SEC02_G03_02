package com.example.loginapps.Student;

public class RegisterString {
    public String s1, s2, s3, s4, c1, c2, c3, c4;

    public RegisterString(){

    }


    public RegisterString(String s1, String s2, String s3, String s4, String c1, String c2,
                          String c3, String c4) {
        this.s1 = s1;
        this.s2 = s2;
        this.s3 = s3;
        this.s4 = s4;

        this.c1 = c1;
        this.c2 = c2;
        this.c3 = c3;
        this.c4 = c4;
    }

    public String getS1() {
        return s1;
    }

    public String getS2() {
        return s2;
    }

    public String getS3() {
        return s3;
    }

    public String getS4() {
        return s4;
    }

    public String getC1() {
        return c1;
    }

    public String getC2() {
        return c2;
    }

    public String getC3() {
        return c3;
    }

    public String getC4() {
        return c4;
    }
}

